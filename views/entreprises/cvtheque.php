                <h2>Envoyer son CV</h2>

                <p>Merci de nous envoyer votre CV en respectant les règles suivantes :
                    <ul>
                      <li>Le titre devra respecter le formalisme suivant : <pre>CV_ANNEE_MOIS_PRENOM_NOM_CODELANGUE.pdf</pre></li>
                      <li><strong>Préciser votre promotion</strong> dans le mail</li>
                      <li>Le code langue pourra être par exemple : FR, EN, ES, ainsi de suite</li>
                      <li>Envoyer votre CV au <strong>format PDF</strong></li>
                  </ul>
                </p>

                <p>Exemples :
                    <pre>
CV_2015_09_JEREMY_WIMSINGUES_EN.pdf
CV_2015_10_JEREMY_WIMSINGUES_ES.pdf
CV_2015_10_JULIEN_BARA_EN.pdf
...
                    </pre>
                </p>

                <p>
                  Il est évident que nous aimerions éviter les mises à jour <strong>récurrentes</strong>. Merci donc de nous envoyer vos CV en <strong>version finale</strong>. 
                  Les mises à jour ponctuelles sont évidemment possibles, il suffit pour cela de nous renvoyer un email avec votre CV à jour.
                </p>

                <p>Nous supprimerons tous les CV <strong>de plus de 6 mois</strong> afin de ne garder que les CVs les plus à jours. </p>

                <p>
                    Gardez en tête que ce dossier sera <strong>public</strong> ! Si vous avez un problème avec la confidentialité ou la collecte des données, il est de votre responsabilité d'<strong>adapter votre CV</strong>. Nous ne sommes pas responsables de l'utilisation qui pourra être faite des CVs déposés sur le Drive.
                </p>

