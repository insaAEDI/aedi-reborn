                        <h1>L'Association</h1>
                        <p>L'AEDI, l'Association des Etudiants du Département Informatique, est une figure incontournable de la vie du Département et de la faune associative de l'INSA de Lyon. Depuis 30 ans, celle-ci accompagne les étudiants en Informatique à travers leur parcours, prodiguant de multiples services et organisant des évènements variés.</p>
                        <p>Comme toute association Loi 1901, l'AEDI n'est pas à but lucratif. L'inscription est <em>gratuite</em> pour tous les étudiants IF, l'association vivant grâce aux actions qu'elle mène, ainsi qu'aux multiples partenariats tissés, notamment avec le <a title="Site Web du Département Informatique" href="http://if.insa-lyon.fr">Département</a> et les <a title="En savoir plus sur le parrainage" href="/entreprises/parrainage">parrains de promotion</a>.</p>
                        <p>Chaque année, une équipe d'étudiants volontaires est élue pour reprendre sa tête, apportant ainsi de nouvelles pierres à l'édifice.</p>
                    </div>
                    <div class="well">
                        <h2><span class="glyphicon glyphicon-star" aria-hidden="true"></span> Notre Mission</h2>
                        <p>L'AEDI a pour rôle de <strong>renforcer la cohésion entre les étudiants du Département Informatique, les aider dans leur cursus, et établir des contacts privilégiés avec les entreprises.</strong></p>
                        <p>Pour cela, l'AEDI profite aujourd'hui d'un riche <strong>réseau</strong> construit au cours des ans, regroupant anciens élèves investis, entreprises clés du monde informatique, et enseignants motivés. Ces ressources, mises à disposition à travers de nombreux évènements fédérateurs, comme les <a title="En savoir plus sur les RIF" href="/entreprises/RIFs">Rencontres IF</a> ou les <a title="En savoir plus sur les conférences" href="/entreprises/conferences">conférences</a>, offrent des opportunités en or aux étudiants de découvrir les acteurs de l'Informatique, et leur futur employeur. Réciproquement, cela permet aux entreprises de renforcer leur présence et découvrir des profils intéressants.</p>
                        <p>S'il est primordial pour les élèves de profiter de ces occasions et de se projeter dans le futur, l'AEDI a aussi conscience de l'importance, parfois, de simplement profiter du présent, de ces années étudiantes qui passent bien trop vite. Dans cette optique sont régulierement organisés des <strong>évènements récréatifs</strong>. Intégration des nouveaux venus, sorties en ville, goûters, activités sportives, ... Toutes les occasions sont bonnes pour renforcer les liens au sein du Département, mais aussi avec l'exterieur, grâce aux bonnes relations développées avec d'autres associations étudiantes lyonnaises.</p>
                        <p>L'AEDI s'est donc donnée comme vocation d'être un acteur clé du Département, en aidant les étudiants dans tous les aspects de leurs etudes.</p>

                        <h2><span class="glyphicon glyphicon-heart" aria-hidden="true"></span> Nos Valeurs</h2>
                        <p>L'AEDI a fait le vœu de croire profondément dans <strong>le potentiel des étudiants</strong> du Département Informatique. C'est pourquoi nous nous efforçons d'apporter le terreau, les opportunités, destinés à faire valoir ces talents. La <strong>diversité</strong> des profils au sein des promotions est également une caractéristique que nous chérissons et encourageons, afin de favoriser une atmosphère d'échanges et de camaraderie plutôt que de compétition.</p>
                        <p>Enfin, pur produit de <strong>l'esprit associatif</strong> de l'INSA-Lyon, l'AEDI a à cœur de faire prospérer celui-ci, en encourageant les initiatives étudiantes et les partenariats inter- et extra-campus.</p>
