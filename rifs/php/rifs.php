				<h1>Rencontres IF</h1>
				<p>
					Les <span class="hero_motcle">Rencontres IF</span>, ou <span class="hero_motcle">RIFs</span>, sont une manifestation propre au Département Informatique de l'INSA de Lyon. Elles permettent, le temps d'une journée, d'élaborer un lien privilégié entre les entreprises et les étudiants.
				</p>
				<p>
					Fort de son succès depuis plus de <strong>dix ans</strong>, cet évènement se renouvelle chaque année en recherchant toujours plus de qualité dans sa prestation.
